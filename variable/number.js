// 변수에 숫자를 넣는 방법

let integer = 123;
let negative = -123;
let double = 1.23;
console.log(integer);
console.log(negative);
console.log(double);

let binary = 0b10101111;
let octal = 0o345;
let next = 0x3b;
console.log(binary);
console.log(octal);
console.log(next);

console.log(0 / 123);
console.log(123 / 0);
console.log(-123 / 0);
console.log(123 / "text");
