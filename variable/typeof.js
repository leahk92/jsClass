// typeof : 데이터 타입 확인
// 제어문에 쓰인다.
let variable;
console.log(typeof variable); //undefined

variable = "";
console.log(typeof variable); //string

variable = 119;
console.log(typeof variable); //number

variable = {};
console.log(typeof variable); //object

variable = function () {};
console.log(typeof variable); //function
